
package Dev_J110;


/*Класс SquareX со статическим методом static double getValue(double x, double y). Функциональный интерфейс 
будет реализован через ссылку на этот статический метод.*/
public class SumX2Y3 {

    public static double getValue(double x, double y) { 
        return Math.pow(x, 2) + Math.pow(y, 3);
    } 
}
